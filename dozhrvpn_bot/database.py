    async def apply_promo_code(self, user_id: int, code: str) -> dict[str, Any]:
        async with self._connection() as conn:
            promo = await self._fetchone("SELECT * FROM promo_codes WHERE code = ?", (code.upper(),), conn=conn)
            if promo is None:
                raise ValueError("Такого промокода нет.")
            if int(promo["is_active"]) != 1:
                raise ValueError("Промокод выключен.")
            if promo["expires_at"] and from_iso(promo["expires_at"]) < utc_now():
                raise ValueError("Срок действия промокода закончился.")
            if int(promo["max_uses"]) > 0 and int(promo["uses_count"]) >= int(promo["max_uses"]):
                raise ValueError("Лимит активаций у промокода исчерпан.")

            exists = await self._fetchone(
                "SELECT 1 FROM promo_redemptions WHERE promo_id = ? AND user_id = ?",
                (promo["id"], user_id),
                conn=conn,
            )
            if exists is not None:
                raise ValueError("Этот промокод уже активирован.")

            await self._execute(
                "INSERT INTO promo_redemptions (promo_id, user_id, redeemed_at) VALUES (?, ?, ?)",
                (promo["id"], user_id, to_iso(utc_now())),
                conn=conn,
            )
            await self._execute(
                "UPDATE promo_codes SET uses_count = uses_count + 1 WHERE id = ?",
                (promo["id"],),
                conn=conn,
            )

            bonus_days = int(promo["bonus_days"])
            discount_percent = int(promo["discount_percent"])
            if bonus_days > 0:
                await self._execute(
                    """
                    UPDATE users
                    SET bonus_days_balance = bonus_days_balance + ?,
                        total_bonus_days_earned = total_bonus_days_earned + ?
                    WHERE id = ?
                    """,
                    (bonus_days, bonus_days, user_id),
                    conn=conn,
                )
            if discount_percent > 0:
                await self._execute(
                    """
                    UPDATE users
                    SET pending_discount_percent = CASE
                        WHEN pending_discount_percent < ? THEN ?
                        ELSE pending_discount_percent
                    END
                    WHERE id = ?
                    """,
                    (discount_percent, discount_percent, user_id),
                    conn=conn,
                )
            await conn.commit()
            return promo

    async def register_referral_reward(self, invited_user_id: int, order_id: int, reward_days: int) -> int | None:
        async with self._connection() as conn:
            invited = await self._fetchone("SELECT * FROM users WHERE id = ?", (invited_user_id,), conn=conn)
            if not invited or invited["referred_by"] is None:
                return None

            exists = await self._fetchone(
                "SELECT 1 FROM referral_rewards WHERE invited_user_id = ?",
                (invited_user_id,),
                conn=conn,
            )
            if exists is not None:
                return None

            referrer_id = int(invited["referred_by"])
            await self._execute(
                """
                INSERT INTO referral_rewards (
                    referrer_user_id, invited_user_id, order_id, reward_days, created_at
                )
                VALUES (?, ?, ?, ?, ?)
                """,
                (referrer_id, invited_user_id, order_id, reward_days, to_iso(utc_now())),
                conn=conn,
            )
            await self._execute(
                """
                UPDATE users
                SET bonus_days_balance = bonus_days_balance + ?,
                    total_bonus_days_earned = total_bonus_days_earned + ?
                WHERE id = ?
                """,
                (reward_days, reward_days, referrer_id),
                conn=conn,
            )
            await conn.commit()
            return referrer_id

