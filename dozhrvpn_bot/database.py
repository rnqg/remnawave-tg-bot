    async def search_users(self, query: str, limit: int = 10) -> list[dict[str, Any]]:
        normalized = query.strip()
        if not normalized:
            return []

        if normalized.isdigit():
            value = int(normalized)
            rows = await self._fetchall(
                """
                SELECT *
                FROM users
                WHERE telegram_id = ? OR id = ?
                ORDER BY id DESC
                LIMIT ?
                """,
                (value, value, limit),
            )
        else:
            username_query = normalized.lstrip("@").lower()
            like_query = f"%{username_query}%"
            name_like_query = f"%{normalized.lower()}%"
            rows = await self._fetchall(
                """
                SELECT *
                FROM users
                WHERE UPPER(ref_code) = ?
                   OR LOWER(COALESCE(username, '')) = ?
                   OR LOWER(COALESCE(username, '')) LIKE ?
                   OR LOWER(COALESCE(first_name, '')) LIKE ?
                   OR LOWER(COALESCE(last_name, '')) LIKE ?
                ORDER BY last_seen_at DESC, id DESC
                LIMIT ?
                """,
                (
                    normalized.upper(),
                    username_query,
                    like_query,
                    name_like_query,
                    name_like_query,
                    limit,
                ),
            )

        return rows

