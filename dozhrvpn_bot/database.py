    async def get_profile_stats(self, user_id: int) -> dict[str, Any]:
        user = await self.get_user_by_id(user_id)
        if not user:
            return {}

        referral_count_row = await self._fetchone(
            "SELECT COUNT(*) AS count FROM users WHERE referred_by = ?",
            (user_id,),
        )
        paid_referral_row = await self._fetchone(
            "SELECT COUNT(*) AS count FROM referral_rewards WHERE referrer_user_id = ?",
            (user_id,),
        )
        orders_row = await self._fetchone(
            "SELECT COUNT(*) AS count FROM orders WHERE user_id = ? AND status = 'activated'",
            (user_id,),
        )
        total_orders_row = await self._fetchone(
            "SELECT COUNT(*) AS count FROM orders WHERE user_id = ?",
            (user_id,),
        )
        spent_total_row = await self._fetchone(
            """
            SELECT COALESCE(SUM(CAST(final_amount AS REAL)), 0) AS total
            FROM orders
            WHERE user_id = ? AND status = 'activated'
            """,
            (user_id,),
        )

        return {
            "user": user,
            "referrals_total": int(referral_count_row["count"]) if referral_count_row else 0,
            "paid_referrals": int(paid_referral_row["count"]) if paid_referral_row else 0,
            "paid_orders": int(orders_row["count"]) if orders_row else 0,
            "orders_total": int(total_orders_row["count"]) if total_orders_row else 0,
            "spent_total": float(spent_total_row["total"]) if spent_total_row else 0.0,
        }

