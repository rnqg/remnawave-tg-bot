from __future__ import annotations

import json
import secrets
from collections.abc import AsyncIterator, Sequence
from contextlib import asynccontextmanager
from datetime import UTC, datetime, timedelta
from typing import Any

from psycopg.rows import dict_row
from psycopg_pool import AsyncConnectionPool


def utc_now() -> datetime:
    return datetime.now(UTC)


def to_iso(dt: datetime | None) -> str | None:
    if dt is None:
        return None
    if dt.tzinfo is None:
        dt = dt.replace(tzinfo=UTC)
    return dt.astimezone(UTC).isoformat()


def from_iso(value: str | None) -> datetime | None:
    if not value:
        return None
    return datetime.fromisoformat(value)


class Database:
    def __init__(self, database_url: str) -> None:
        self.database_url = database_url
        self.pool: AsyncConnectionPool | None = None

    @staticmethod
    def _sql(query: str) -> str:
        return query.replace("?", "%s")

    @asynccontextmanager
    async def _connection(self) -> AsyncIterator[Any]:
        if self.pool is None:
            raise RuntimeError("Database pool is not initialized.")
        async with self.pool.connection() as conn:
            yield conn

    async def connect(self) -> None:
        self.pool = AsyncConnectionPool(
            conninfo=self.database_url,
            min_size=1,
            max_size=10,
            open=False,
            kwargs={"row_factory": dict_row},
        )
        await self.pool.open(wait=True)
        async with self._connection() as conn:
            await self._create_schema(conn)
            await conn.commit()

    async def close(self) -> None:
        if self.pool is not None:
            await self.pool.close()
            self.pool = None

    async def _execute(self, query: str, params: Sequence[Any] = (), *, conn: Any) -> Any:
        return await conn.execute(self._sql(query), params)

    async def _fetchone(
        self,
        query: str,
        params: Sequence[Any] = (),
        *,
        conn: Any | None = None,
    ) -> dict[str, Any] | None:
        if conn is None:
            async with self._connection() as local_conn:
                return await self._fetchone(query, params, conn=local_conn)
        cursor = await self._execute(query, params, conn=conn)
        row = await cursor.fetchone()
        await cursor.close()
        return row

    async def _fetchall(
        self,
        query: str,
        params: Sequence[Any] = (),
        *,
        conn: Any | None = None,
    ) -> list[dict[str, Any]]:
        if conn is None:
            async with self._connection() as local_conn:
                return await self._fetchall(query, params, conn=local_conn)
        cursor = await self._execute(query, params, conn=conn)
        rows = await cursor.fetchall()
        await cursor.close()
        return list(rows)

    async def _create_schema(self, conn: Any) -> None:
        statements = (
            """
            CREATE TABLE IF NOT EXISTS users (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                telegram_id BIGINT NOT NULL UNIQUE,
                username TEXT,
                first_name TEXT,
                last_name TEXT,
                ref_code TEXT NOT NULL UNIQUE,
                referred_by BIGINT REFERENCES users(id),
                joined_at TEXT NOT NULL,
                last_seen_at TEXT NOT NULL,
                is_channel_member INTEGER NOT NULL DEFAULT 0,
                bonus_days_balance INTEGER NOT NULL DEFAULT 0,
                pending_discount_percent INTEGER NOT NULL DEFAULT 0,
                total_bonus_days_earned INTEGER NOT NULL DEFAULT 0,
                remnawave_uuid TEXT,
                remnawave_short_uuid TEXT,
                remnawave_subscription_url TEXT,
                subscription_expires_at TEXT
            )
            """,
            """
            CREATE TABLE IF NOT EXISTS orders (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                user_id BIGINT NOT NULL REFERENCES users(id),
                plan_key TEXT NOT NULL,
                plan_name TEXT NOT NULL,
                days INTEGER NOT NULL,
                base_amount NUMERIC(12, 2) NOT NULL,
                final_amount NUMERIC(12, 2) NOT NULL,
                currency_type TEXT NOT NULL,
                fiat TEXT,
                asset TEXT,
                accepted_assets TEXT,
                promo_code TEXT,
                discount_percent INTEGER NOT NULL DEFAULT 0,
                payment_provider TEXT NOT NULL DEFAULT 'crypto_bot',
                payment_invoice_id TEXT,
                payment_label TEXT,
                payment_url TEXT,
                payment_extra_url TEXT,
                payment_status TEXT,
                crypto_invoice_id BIGINT,
                crypto_invoice_hash TEXT,
                crypto_bot_invoice_url TEXT,
                crypto_mini_app_invoice_url TEXT,
                crypto_web_app_invoice_url TEXT,
                crypto_invoice_status TEXT,
                status TEXT NOT NULL,
                created_at TEXT NOT NULL,
                paid_at TEXT,
                activated_at TEXT,
                raw_invoice_json TEXT
            )
            """,
            """
            CREATE TABLE IF NOT EXISTS promo_codes (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                code TEXT NOT NULL UNIQUE,
                bonus_days INTEGER NOT NULL DEFAULT 0,
                discount_percent INTEGER NOT NULL DEFAULT 0,
                max_uses INTEGER NOT NULL DEFAULT 0,
                uses_count INTEGER NOT NULL DEFAULT 0,
                is_active INTEGER NOT NULL DEFAULT 1,
                expires_at TEXT,
                description TEXT,
                created_at TEXT NOT NULL
            )
            """,
            """
            CREATE TABLE IF NOT EXISTS promo_redemptions (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                promo_id BIGINT NOT NULL REFERENCES promo_codes(id),
                user_id BIGINT NOT NULL REFERENCES users(id),
                redeemed_at TEXT NOT NULL,
                UNIQUE (promo_id, user_id)
            )
            """,
            """
            CREATE TABLE IF NOT EXISTS referral_rewards (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                referrer_user_id BIGINT NOT NULL REFERENCES users(id),
                invited_user_id BIGINT NOT NULL UNIQUE REFERENCES users(id),
                order_id BIGINT NOT NULL REFERENCES orders(id),
                reward_days INTEGER NOT NULL,
                created_at TEXT NOT NULL
            )
            """,
            """
            CREATE TABLE IF NOT EXISTS settings (
                key TEXT PRIMARY KEY,
                value TEXT NOT NULL,
                updated_at TEXT NOT NULL
            )
            """,
            """
            CREATE TABLE IF NOT EXISTS broadcast_campaigns (
                id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                name TEXT NOT NULL,
                source_chat_id BIGINT NOT NULL,
                source_message_id BIGINT NOT NULL,
                buttons_json TEXT,
                interval_hours INTEGER NOT NULL,
                is_active INTEGER NOT NULL DEFAULT 1,
                created_at TEXT NOT NULL,
                last_sent_at TEXT,
                next_run_at TEXT NOT NULL,
                last_success_count INTEGER NOT NULL DEFAULT 0,
                last_failed_count INTEGER NOT NULL DEFAULT 0
            )
            """,
        )
        for statement in statements:
            await self._execute(statement, conn=conn)

        await self._ensure_column("orders", "payment_provider", "TEXT NOT NULL DEFAULT 'crypto_bot'", conn=conn)
        await self._ensure_column("orders", "payment_invoice_id", "TEXT", conn=conn)
        await self._ensure_column("orders", "payment_label", "TEXT", conn=conn)
        await self._ensure_column("orders", "payment_url", "TEXT", conn=conn)
        await self._ensure_column("orders", "payment_extra_url", "TEXT", conn=conn)
        await self._ensure_column("orders", "payment_status", "TEXT", conn=conn)
        await self._execute(
            """
            UPDATE orders
            SET payment_provider = 'crypto_bot'
            WHERE payment_provider IS NULL OR TRIM(payment_provider) = ''
            """,
            conn=conn,
        )
        await self._execute(
            """
            UPDATE orders
            SET payment_invoice_id = COALESCE(payment_invoice_id, CAST(crypto_invoice_id AS TEXT)),
                payment_label = COALESCE(payment_label, crypto_invoice_hash),
                payment_url = COALESCE(payment_url, crypto_bot_invoice_url),
                payment_extra_url = COALESCE(payment_extra_url, crypto_mini_app_invoice_url),
                payment_status = COALESCE(payment_status, crypto_invoice_status)
            """,
            conn=conn,
        )

    async def _ensure_column(self, table: str, column: str, definition: str, *, conn: Any) -> None:
        row = await self._fetchone(
            """
            SELECT 1
            FROM information_schema.columns
            WHERE table_schema = 'public' AND table_name = ? AND column_name = ?
            """,
            (table, column),
            conn=conn,
        )
        if row is not None:
            return
        await self._execute(f"ALTER TABLE {table} ADD COLUMN {column} {definition}", conn=conn)

    async def _generate_unique_ref_code(self, *, conn: Any) -> str:
        while True:
            code = secrets.token_urlsafe(6).replace("-", "").replace("_", "").upper()[:10]
            row = await self._fetchone("SELECT 1 FROM users WHERE ref_code = ?", (code,), conn=conn)
            if row is None:
                return code

    async def get_or_create_user(
        self,
        telegram_id: int,
        username: str | None,
        first_name: str | None,
        last_name: str | None,
        referrer_code: str | None = None,
    ) -> dict[str, Any]:
        async with self._connection() as conn:
            existing = await self._fetchone(
                "SELECT * FROM users WHERE telegram_id = ?",
                (telegram_id,),
                conn=conn,
            )
            now_iso = to_iso(utc_now())

            if existing:
                await self._execute(
                    """
                    UPDATE users
                    SET username = ?, first_name = ?, last_name = ?, last_seen_at = ?
                    WHERE telegram_id = ?
                    """,
                    (username, first_name, last_name, now_iso, telegram_id),
                    conn=conn,
                )
                await conn.commit()
                row = await self._fetchone(
                    "SELECT * FROM users WHERE telegram_id = ?",
                    (telegram_id,),
                    conn=conn,
                )
                return row or {}

            referred_by: int | None = None
            if referrer_code:
                referrer = await self._fetchone(
                    "SELECT id, telegram_id FROM users WHERE ref_code = ?",
                    (referrer_code.upper(),),
                    conn=conn,
                )
                if referrer and referrer["telegram_id"] != telegram_id:
                    referred_by = int(referrer["id"])

            ref_code = await self._generate_unique_ref_code(conn=conn)
            created = await self._fetchone(
                """
                INSERT INTO users (
                    telegram_id, username, first_name, last_name, ref_code,
                    referred_by, joined_at, last_seen_at
                )
                VALUES (?, ?, ?, ?, ?, ?, ?, ?)
                RETURNING *
                """,
                (
                    telegram_id,
                    username,
                    first_name,
                    last_name,
                    ref_code,
                    referred_by,
                    now_iso,
                    now_iso,
                ),
                conn=conn,
            )
            await conn.commit()
            return created or {}

