    async def search_orders(self, query: str, limit: int = 10) -> list[dict[str, Any]]:
        normalized = query.strip()
        if not normalized:
            return []

        if normalized.isdigit():
            value = int(normalized)
            rows = await self._fetchall(
                """
                SELECT o.*, u.telegram_id
                FROM orders o
                JOIN users u ON u.id = o.user_id
                WHERE o.id = ? OR u.telegram_id = ?
                ORDER BY o.id DESC
                LIMIT ?
                """,
                (value, value, limit),
            )
        else:
            like_query = f"%{normalized.lower()}%"
            rows = await self._fetchall(
                """
                SELECT o.*, u.telegram_id
                FROM orders o
                JOIN users u ON u.id = o.user_id
                WHERE LOWER(o.plan_name) LIKE ?
                   OR LOWER(o.plan_key) LIKE ?
                   OR LOWER(COALESCE(o.promo_code, '')) LIKE ?
                   OR LOWER(o.status) LIKE ?
                ORDER BY o.id DESC
                LIMIT ?
                """,
                (like_query, like_query, like_query, like_query, limit),
            )

        return rows

