    async def update_order_invoice(self, order_id: int, invoice: dict[str, Any]) -> None:
        invoice_id = invoice.get("invoice_id")
        invoice_id_text = None if invoice_id in (None, "") else str(invoice_id)
        payment_status = invoice.get("status")
        async with self._connection() as conn:
            await self._execute(
                """
                UPDATE orders
                SET payment_invoice_id = ?, payment_label = ?, payment_url = ?,
                    payment_extra_url = ?, payment_status = ?,
                    crypto_invoice_id = ?, crypto_invoice_hash = ?, crypto_bot_invoice_url = ?,
                    crypto_mini_app_invoice_url = ?, crypto_web_app_invoice_url = ?,
                    crypto_invoice_status = ?, raw_invoice_json = ?
                WHERE id = ?
                """,
                (
                    invoice_id_text,
                    invoice.get("label"),
                    invoice.get("pay_url"),
                    invoice.get("extra_url"),
                    payment_status,
                    int(invoice_id) if str(invoice_id).isdigit() else None,
                    invoice.get("label"),
                    invoice.get("pay_url"),
                    invoice.get("extra_url"),
                    invoice.get("web_app_url"),
                    payment_status,
                    json.dumps(invoice.get("raw") or invoice, ensure_ascii=False),
                    order_id,
                ),
                conn=conn,
            )
            await conn.commit()

